    private void addBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBTActionPerformed
        // TODO add your handling code here:

        int idx = idMTxt.getSelectedIndex();
        String idM = new MotelController().getIdMotel(idMTxt.getItemAt(idx));

        if (priceRMTxt.getText().equals("")) {
            errMax.setText(" ");
            errPrice.setText("Please enter Price room");
        } else if (!priceRMTxt.getText().matches("[0-9]+")) {
            errMax.setText(" ");
            errPrice.setText("Price room contain numeric characters");
        } else if (Double.valueOf(priceRMTxt.getText()) < 100000) {
            errMax.setText(" ");
            errPrice.setText("Price room not less than 100.000 VND");
        } else if (maxTxt.getText().equals("")) {
            errMax.setText("Please enter Max quantity");
            errPrice.setText(" ");
        } else if (!maxTxt.getText().matches("[0-9]+")) {
            errMax.setText("Max quantity contain numeric characters");
            errPrice.setText(" ");
        } else if (Integer.valueOf(maxTxt.getText()) < 1) {
            errMax.setText("Max quantity not less than 1");
            errPrice.setText(" ");
        } else {
            errMax.setText(" ");
            errPrice.setText(" ");
            
            RoomModel rm = new RoomModel(idRTxt.getText(), idM, Double.valueOf(priceRMTxt.getText()), Integer.valueOf(maxTxt.getText()), 0, "TT1");
            if (roomDAO.insertRoom(rm)) {
                JOptionPane.showMessageDialog(rootPane, "Successfully!!!");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Failed!!!");
                this.dispose();
            }
            
        }
    }//GEN-LAST:event_addBTActionPerformed

