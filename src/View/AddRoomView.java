    private void setItems() {
        for (MotelModel i : listMotel) {
            this.idMTxt.addItem(i.getNameMotel());
        }
        int idx = idMTxt.getSelectedIndex();
        String idM = new MotelController().getIdMotel(idMTxt.getItemAt(idx));
        String tmp = roomDAO.getMaxIdRoom(idM);
        int maxTmp = 1;
        if(!tmp.equals("null")) {
            maxTmp =  Integer.valueOf(tmp.substring(2)) + 1;
        }
        
        if(maxTmp < 10) {
            this.idRTxt.setText(idM + "_00" + maxTmp);
        } else if (maxTmp < 100) {
            this.idRTxt.setText(idM + "_0" + maxTmp);
        } else {
            this.idRTxt.setText(idM + "_" + maxTmp      );
        }
        this.idRTxt.setEditable(false);
        this.stsTxt.setText("Chưa có người thuê");
        this.stsTxt.setEditable(false);
    }

